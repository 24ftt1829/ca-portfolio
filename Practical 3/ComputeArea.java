public class ComputeArea {
    public static void main(String[] args) {
     double b;double h;double pi;double r;
     b=4.0;
     h=6.0;
     pi=3.14;
     r=8.0;
     double areaOfSquare=(b*b);
     double areaOfTriangle=(0.5*b*h);
     double areaOfCircle=(pi*r*r);
     System.out.println("The area of square is "+ areaOfSquare + "\nThe area of triangle is "+ areaOfTriangle+ "\nThe area of circle is "+ areaOfCircle);
    }
}
 